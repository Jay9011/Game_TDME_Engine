#pragma once

#include <Core/CoreTypes.h>

namespace TDME
{
    /**
     * @brief 텍스처 포맷
     * @details 텍스처 데이터의 비트 구조를 나타내는 열거형
     */
    enum class ETextureFormat : uint8
    {
        Unknown,

        //////////////////////////////////////////////////////////////
        // 8비트 포맷
        //////////////////////////////////////////////////////////////
        R8,       // 단일 채널 8비트
        R8G8B8A8, // RGBA 각 8비트 (총 32비트)

        //////////////////////////////////////////////////////////////
        // 16비트 포맷
        //////////////////////////////////////////////////////////////
        R16,          // 단일 채널 16비트
        R16G16B16A16, // RGBA 각 16비트 (총 64비트)

        //////////////////////////////////////////////////////////////
        // 32비트 float 포맷
        //////////////////////////////////////////////////////////////
        R32Float,          // 단일 채널 32비트 float
        R32G32B32A32Float, // RGBA 각 32비트 float

        //////////////////////////////////////////////////////////////
        // 압축 포맷
        //////////////////////////////////////////////////////////////
        BC1, // DXT1 (RGB, 1비트 알파)
        BC3, // DXT5 (RGBA)

        //////////////////////////////////////////////////////////////
        // 깊이/스텐실 포맷
        //////////////////////////////////////////////////////////////
        D24S8,    // 24비트 깊이 + 8비트 스텐실
        D32Float, // 32비트 깊이
    };
} // namespace TDME